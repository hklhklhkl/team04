package com.ztkj.resource.entity;

import java.util.Date;

public class ResourceType {
	private int exploreTypeId;
	private String exploreTypeName;
	private String exploreTypeMan;
	private Date exploreTypeDate;
	private int exploreTypeState;
	public String getExploreTypeName() {
		return exploreTypeName;
	}
	public void setExploreTypeName(String exploreTypeName) {
		this.exploreTypeName = exploreTypeName;
	}
	public String getExploreTypeMan() {
		return exploreTypeMan;
	}
	public void setExploreTypeMan(String exploreTypeMan) {
		this.exploreTypeMan = exploreTypeMan;
	}
	public Date getExploreTypeDate() {
		return exploreTypeDate;
	}
	public void setExploreTypeDate(Date exploreTypeDate) {
		this.exploreTypeDate = exploreTypeDate;
	}
	public int getExploreTypeState() {
		return exploreTypeState;
	}
	public void setExploreTypeState(int exploreTypeState) {
		this.exploreTypeState = exploreTypeState;
	}
	public int getExploreTypeId() {
		return exploreTypeId;
	}
	public void setExploreTypeId(int exploreTypeId) {
		this.exploreTypeId = exploreTypeId;
	}
	
	

}
